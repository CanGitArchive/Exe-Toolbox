def has_useful_alpha(img) -> bool:
    if img.mode not in ("RGBA", "LA"):
        return False
    alpha = img.getchannel("A")
    extrema = alpha.getextrema()
    return extrema[0] < 255


def auto_remove_border_background(img, mode="light"):
    """
    Flood-fill transparent only the background connected to the image borders.

    mode="light" removes near-white/light neutral border backgrounds.
    mode="dark" removes near-black/dark neutral border backgrounds.

    Border-connected only: it does not remove every black/white pixel inside the icon.
    """
    img = img.convert("RGBA")
    pix = img.load()
    w, h = img.size

    def is_background(pixel):
        r, g, b, a = pixel
        if a < 10:
            return True

        mx = max(r, g, b)
        mn = min(r, g, b)
        spread = mx - mn

        if mode == "dark":
            return mx <= 38 and spread <= 22

        return mx >= 220 and spread <= 24

    visited = set()
    q = deque()

    for x in range(w):
        for y in (0, h - 1):
            if (x, y) not in visited and is_background(pix[x, y]):
                visited.add((x, y))
                q.append((x, y))

    for y in range(h):
        for x in (0, w - 1):
            if (x, y) not in visited and is_background(pix[x, y]):
                visited.add((x, y))
                q.append((x, y))

    while q:
        x, y = q.popleft()
        r, g, b, a = pix[x, y]
        pix[x, y] = (r, g, b, 0)

        for nx, ny in ((x - 1, y), (x + 1, y), (x, y - 1), (x, y + 1)):
            if 0 <= nx < w and 0 <= ny < h and (nx, ny) not in visited:
                if is_background(pix[nx, ny]):
                    visited.add((nx, ny))
                    q.append((nx, ny))

    return img


def auto_remove_light_border_background(img):
    return auto_remove_border_background(img, mode="light")


def auto_remove_dark_border_background(img):
    return auto_remove_border_background(img, mode="dark")


def write_ico_bytes_safely(path: Path, data: bytes, log_func=None) -> None:
    """
    Write ICO bytes via a temporary file, then replace the final file.

    This avoids the Windows/Pillow direct-save issue we hit before, while not
    changing the Python-to-EXE build logic at all.
    """
    path = Path(path)
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.parent / f".{path.name}.tmp_write"

    if tmp.exists():
        try:
            tmp.unlink()
        except Exception:
            pass

    if log_func:
        log_func(f"Writing temp ICO: {tmp}")

    with open(str(tmp), "wb") as f:
        f.write(data)

    if log_func:
        log_func(f"Replacing final ICO: {path}")

    try:
        os.replace(str(tmp), str(path))
    except Exception:
        if path.exists():
            try:
                path.unlink()
            except Exception:
                pass
        shutil.move(str(tmp), str(path))


def crop_and_square(img, padding_ratio=0.08):
    from PIL import Image

    img = img.convert("RGBA")
    alpha = img.getchannel("A")
    bbox = alpha.getbbox()

    if bbox:
        left, top, right, bottom = bbox
        bw = right - left
        bh = bottom - top
        pad = int(max(bw, bh) * padding_ratio)

        left = max(0, left - pad)
        top = max(0, top - pad)
        right = min(img.width, right + pad)
        bottom = min(img.height, bottom + pad)

        img = img.crop((left, top, right, bottom))

    side = max(img.width, img.height)
    canvas = Image.new("RGBA", (side, side), (0, 0, 0, 0))
    canvas.paste(img, ((side - img.width) // 2, (side - img.height) // 2), img)
    return canvas


def convert_png_to_ico(
    input_png: Path,
    output_ico: Path,
    remove_light_border=True,
    remove_dark_border=False,
    crop_square=True,
    padding_ratio=0.08,
    python_cmd: list[str] | None = None,
    log_func=None,
) -> Path:
    ensure_pillow(python_cmd=python_cmd, log_func=log_func)
    from PIL import Image

    input_png = normalize_user_path(input_png)
    output_ico = normalize_user_path(output_ico)

    if input_png.suffix.lower() != ".png":
        raise ValueError("Input image must be a .png file.")

    if not input_png.exists():
        raise FileNotFoundError(f"PNG not found: {input_png}")

    output_ico.parent.mkdir(parents=True, exist_ok=True)

    if log_func:
        log_func(f"Input PNG: {input_png}")
        log_func(f"Output ICO: {output_ico}")

    img_original = Image.open(str(input_png))

    if has_useful_alpha(img_original):
        if log_func:
            log_func("Detected real transparency. Preserving alpha.")
        img = img_original.convert("RGBA")
    else:
        img = img_original.convert("RGBA")
        removed_any = False

        if remove_light_border:
            if log_func:
                log_func("No useful transparency detected. Removing light/white/checkerboard border background.")
            img = auto_remove_light_border_background(img)
            removed_any = True

        if remove_dark_border:
            if log_func:
                log_func("Removing dark/black border background. Conservative border-connected removal only.")
            img = auto_remove_dark_border_background(img)
            removed_any = True

        if not removed_any and log_func:
            log_func("No useful transparency detected. Keeping original background.")

    if crop_square:
        if log_func:
            log_func(f"Cropping visible area and making square canvas. Padding: {padding_ratio:.2f}")
        img = crop_and_square(img, padding_ratio=padding_ratio)

    sizes = [(16, 16), (24, 24), (32, 32), (48, 48), (64, 64), (128, 128), (256, 256)]

    buffer = BytesIO()
    img.save(buffer, format="ICO", sizes=sizes)
    write_ico_bytes_safely(output_ico, buffer.getvalue(), log_func=log_func)

    if log_func:
        log_func("ICO created successfully.")

    return output_ico


