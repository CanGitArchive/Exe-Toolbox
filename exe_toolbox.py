_REQ_NAME_RE = re.compile(r"^\s*([A-Za-z0-9][A-Za-z0-9._-]*)")


def parse_requirements_packages(req_path: Path) -> set[str]:
    """
    Extract top-level distribution names from a requirements.txt, normalized to
    lower-case with underscores->hyphens (PEP 503-ish), e.g.
    'scikit-learn==1.4 ; python_version>="3.10"' -> 'scikit-learn'.
    Ignores comments, blank lines, -r includes, editable installs.
    """
    names: set[str] = set()
    try:
        text = Path(req_path).read_text(encoding="utf-8", errors="replace")
    except Exception:
        return names

    for raw in text.splitlines():
        line = raw.strip()
        if not line or line.startswith(("#", "-r", "--", "-e ", "-c ")):
            continue
        m = _REQ_NAME_RE.match(line)
        if m:
            names.add(m.group(1).lower().replace("_", "-"))
    return names


def installed_distributions(python_cmd: list[str]) -> set[str]:
    """
    Lower-case, hyphen-normalized set of distribution names installed in the
    given Python. Used to detect 'heavy lib installed but not in reqs' bloat.
    """
    script = (
        "import importlib.metadata, sys\n"
        "for d in importlib.metadata.distributions():\n"
        "    n = d.metadata.get('Name')\n"
        "    if n: sys.stdout.write(n + '\\n')\n"
    )
    try:
        result = subprocess.run(
            python_cmd + ["-c", script],
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            text=True,
            encoding="utf-8",
            errors="replace",
            check=False,
            **windows_no_console_kwargs(),
        )
        return {
            line.strip().lower().replace("_", "-")
            for line in (result.stdout or "").splitlines()
            if line.strip()
        }
    except Exception:
        return set()


def heavy_libs_not_in_requirements(
    python_cmd: list[str], requirements_path: Path | None
) -> list[str]:
    """
    Return the sorted list of HEAVY_LIBS that are installed in the interpreter
    but NOT declared in requirements.txt. An empty list means the venv is lean
    relative to the declared deps.
    """
    installed = installed_distributions(python_cmd)
    declared = parse_requirements_packages(requirements_path) if requirements_path else set()
    heavy_norm = {h.lower().replace("_", "-") for h in HEAVY_LIBS}
    return sorted((heavy_norm & installed) - declared)


def target_imports_module(target_py: Path, module: str) -> bool:
    """
    Cheap textual check: does the target .py (or any .py beside it) contain a
    top-level `import {module}` or `from {module} ...`? Used so the
    'Detect web/Flask data' preset only wires pystray._win32 when pystray is
    actually used, instead of leaking it into every project.
    """
    if not module:
        return False
    pattern = re.compile(rf"^\s*(?:import|from)\s+{re.escape(module)}\b", re.MULTILINE)
    files: list[Path] = []
    target_py = Path(target_py)
    if target_py.is_file():
        files.append(target_py)
    base = target_py.parent if target_py.is_file() else target_py
    if base.is_dir():
        try:
            files.extend(p for p in base.glob("*.py") if p not in files)
        except Exception:
            pass
    for f in files:
        try:
            if pattern.search(f.read_text(encoding="utf-8", errors="ignore")):
                return True
        except Exception:
            pass
    return False


def stale_source_reason(target_py: Path) -> str | None:
    """
    Return a human-readable reason the chosen target_py looks stale, or None.

    Triggers:
      - target_py sits inside a snapshot/archive folder (Version_Control/, etc.)
      - a same-base-name `.py` in the same folder has a newer mtime
        (e.g. selected MyApp_V12.py while MyApp.py is newer next to it)
    """
    target_py = Path(target_py)
    for part in target_py.parts:
        if part.lower() in SNAPSHOT_DIR_NAMES:
            return (
                f"The selected file is inside a snapshot/archive folder ('{part}').\n"
                f"It is probably an OLD copy, not the live source.\n\n"
                f"Selection: {target_py}"
            )

    try:
        target_mtime = target_py.stat().st_mtime
    except Exception:
        return None

    base_stem = re.sub(r"[_-]?[vV]?\d+(?:[._-]\d+)*$", "", target_py.stem)
    if not base_stem:
        return None

    newest_sibling: Path | None = None
    newest_mtime = target_mtime
    try:
        for sib in target_py.parent.glob("*.py"):
            if sib == target_py:
                continue
            sib_stem = re.sub(r"[_-]?[vV]?\d+(?:[._-]\d+)*$", "", sib.stem)
            if sib_stem.lower() != base_stem.lower():
                continue
            try:
                m = sib.stat().st_mtime
            except Exception:
                continue
            if m > newest_mtime:
                newest_mtime = m
                newest_sibling = sib
    except Exception:
        return None

    if newest_sibling is not None:
        return (
            f"A newer same-base-name file exists in the same folder:\n"
            f"  newer: {newest_sibling}  ({format_iso(newest_mtime)})\n"
            f"  selected: {target_py}  ({format_iso(target_mtime)})\n\n"
            f"You may be building an old version."
        )
    return None


def write_build_stamp(
    stamp_path: Path,
    target_py: Path,
    exe_name: str,
    python_cmd: list[str],
    isolated_build: bool,
) -> dict:
    """
    Write EXE_TOOLBOX_BUILD.json next to the staged inputs and return the dict.
    Bundled via --add-data into the EXE root, so 'which source produced this
    EXE?' becomes a one-second sha256 check.
    """
    target_py = Path(target_py)
    st = target_py.stat()
    stamp = {
        "toolbox": APP_NAME,
        "toolbox_version": APP_VERSION,
        "built_at": format_iso(_dt.datetime.now().timestamp()),
        "exe_name": exe_name,
        "source_path": str(target_py.resolve()),
        "source_size_bytes": st.st_size,
        "source_mtime": format_iso(st.st_mtime),
        "source_sha256": sha256_file(target_py),
        "python_version": python_module_version(python_cmd, "sys") or "",
        "pyinstaller_version": python_module_version(python_cmd, "PyInstaller") or "",
        "isolated_build": bool(isolated_build),
        "host_platform": sys.platform,
    }
    # python_module_version of 'sys' returns '' (no __version__); replace with
    # a real reading via platform on the selected interpreter.
    try:
        result = subprocess.run(
            python_cmd + ["-c", "import sys; print(sys.version.split()[0])"],
            stdout=subprocess.PIPE,
            stderr=subprocess.DEVNULL,
            text=True,
            encoding="utf-8",
            errors="replace",
            check=False,
            **windows_no_console_kwargs(),
        )
        stamp["python_version"] = (result.stdout or "").strip()
    except Exception:
        pass

    stamp_path = Path(stamp_path)
    stamp_path.parent.mkdir(parents=True, exist_ok=True)
    stamp_path.write_text(json.dumps(stamp, indent=2), encoding="utf-8")
    return stamp


def create_isolated_venv(
    base_python_cmd: list[str], venv_dir: Path, log_func=None
) -> list[str]:
    """
    Create an isolated venv at `venv_dir` using the user-selected Python, and
    return the python command for that venv. The caller then installs only
    requirements.txt + pyinstaller into it -> guaranteed lean build.
    """
    venv_dir = Path(venv_dir)
    venv_dir.parent.mkdir(parents=True, exist_ok=True)
    code = run_command(
        base_python_cmd + ["-m", "venv", str(venv_dir)],
        log_func=log_func,
    )
    if code != 0:
        raise RuntimeError(f"Could not create isolated venv at {venv_dir} (exit {code}).")

    if sys.platform.startswith("win"):
        iso_python = venv_dir / "Scripts" / "python.exe"
    else:
        iso_python = venv_dir / "bin" / "python"
    if not iso_python.exists():
        raise RuntimeError(f"Isolated venv created but python not found at {iso_python}.")
    return [str(iso_python)]


