def windows_no_console_kwargs() -> dict:
    """
    Hide console windows for subprocesses launched by THIS TOOLBOX.

    This affects:
    - pip install
    - PyInstaller build
    - Test Python
    - python_has_module checks

    This does NOT inject anything into the EXE being built.
    It does NOT add runtime hooks.
    It does NOT patch the target app.
    """
    if not sys.platform.startswith("win"):
        return {}

    kwargs = {
        "creationflags": getattr(subprocess, "CREATE_NO_WINDOW", 0),
    }

    try:
        startupinfo = subprocess.STARTUPINFO()
        startupinfo.dwFlags |= subprocess.STARTF_USESHOWWINDOW
        startupinfo.wShowWindow = 0
        kwargs["startupinfo"] = startupinfo
    except Exception:
        pass

    return kwargs


def run_command(cmd: list[str], cwd: Path | None = None, log_func=None) -> int:
    if log_func:
        log_func("")
        log_func("Running:")
        log_func(" ".join(f'"{c}"' if " " in str(c) else str(c) for c in cmd))
        log_func("")

    process = subprocess.Popen(
        cmd,
        cwd=str(cwd) if cwd else None,
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        encoding="utf-8",
        errors="replace",
        bufsize=1,
        **windows_no_console_kwargs(),
    )

    assert process.stdout is not None
    for line in process.stdout:
        if log_func:
            log_func(line.rstrip())

    return process.wait()


def generate_paddleocr_official_args(python_cmd: list[str]) -> tuple[list[str], str]:
    """
    Generate PaddleOCR packaging arguments using PaddleOCR's official packaging logic.

    Official idea:
      pyinstaller app.py --collect-data paddlex --collect-binaries paddle
      plus --copy-metadata for installed packages that PaddleX checks at runtime.
    """
    script = """
import importlib.metadata
try:
    import paddlex
except Exception as exc:
    raise SystemExit(f"Could not import paddlex: {type(exc).__name__}: {exc}")

deps_all = set(paddlex.utils.deps.BASE_DEP_SPECS.keys())
user_deps = []
for dist in importlib.metadata.distributions():
    name = dist.metadata.get("Name")
    if name:
        user_deps.append(name)

deps_need = []
seen = set()
for dep in user_deps:
    if dep in deps_all and dep.lower() not in seen:
        deps_need.append(dep)
        seen.add(dep.lower())

for dep in ["paddleocr", "paddlepaddle", "paddlex"]:
    if dep.lower() not in seen:
        deps_need.append(dep)
        seen.add(dep.lower())

args = ["--collect-data", "paddlex", "--collect-binaries", "paddle"]
for dep in deps_need:
    args.extend(["--copy-metadata", dep])

print(" ".join(args))
"""
    result = subprocess.run(
        python_cmd + ["-c", script],
        stdout=subprocess.PIPE,
        stderr=subprocess.STDOUT,
        text=True,
        encoding="utf-8",
        errors="replace",
        check=False,
        **windows_no_console_kwargs(),
    )
    output = (result.stdout or "").strip()
    if result.returncode != 0:
        raise RuntimeError(output or f"PaddleOCR args generation failed with exit code {result.returncode}")

    return split_extra_args(output), output


