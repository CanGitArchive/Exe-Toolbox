class BuildWorker(QThread):
    log = pyqtSignal(str)
    finished_ok = pyqtSignal(str)
    failed = pyqtSignal(str)

    def __init__(
        self,
        python_cmd,
        target_py,
        icon_path,
        output_dir,
        exe_name,
        windowed,
        onefile,
        clean,
        install_pyinstaller,
        install_requirements,
        requirements_path,
        extra_pyinstaller_args,
        data_entries,
        hidden_imports,
        appid_hook_enabled,
        app_user_model_id,
        pyqt_icon_hook_enabled,
        embed_build_stamp,
        isolated_build,
        png_remove_light_border,
        png_remove_dark_border,
        png_crop_square,
        png_padding,
    ):
        super().__init__()
        self.python_cmd = python_cmd
        self.target_py = Path(target_py)
        self.icon_path = Path(icon_path) if icon_path else None
        self.output_dir = Path(output_dir)
        self.exe_name = exe_name.strip()
        self.windowed = windowed
        self.onefile = onefile
        self.clean = clean
        self.install_pyinstaller = install_pyinstaller
        self.install_requirements = install_requirements
        self.requirements_path = Path(requirements_path) if requirements_path else None
        self.extra_pyinstaller_args = list(extra_pyinstaller_args or [])
        self.data_entries = [e for e in (data_entries or []) if str(e).strip()]
        self.hidden_imports = list(hidden_imports or [])
        self.appid_hook_enabled = bool(appid_hook_enabled)
        self.app_user_model_id = str(app_user_model_id or "").strip()
        self.pyqt_icon_hook_enabled = bool(pyqt_icon_hook_enabled)
        self.embed_build_stamp = bool(embed_build_stamp)
        self.isolated_build = bool(isolated_build)
        self.png_remove_light_border = png_remove_light_border
        self.png_remove_dark_border = png_remove_dark_border
        self.png_crop_square = png_crop_square
        self.png_padding = png_padding

    def run(self):
        try:
            self._run_build()
        except Exception as exc:
            self.failed.emit(str(exc))

    def _run_build(self):
        if not self.python_cmd:
            raise ValueError("Python command is empty.")

        if not self.target_py.exists():
            raise FileNotFoundError(f"Python file not found: {self.target_py}")

        if self.target_py.suffix.lower() != ".py":
            raise ValueError("Target file must be a .py file.")

        if not self.exe_name:
            self.exe_name = self.target_py.stem

        self.output_dir.mkdir(parents=True, exist_ok=True)

        self.log.emit(f"Target Python file: {self.target_py}")
        self.log.emit(f"Output folder: {self.output_dir}")
        self.log.emit(f"EXE name: {self.exe_name}")
        self.log.emit("")

        # V1.8: provenance, log a short fingerprint of the source we are about
        # to build. Cheap insurance against "stale binary" misdiagnoses.
        try:
            st = self.target_py.stat()
            self.log.emit(f"Source size:   {st.st_size} bytes")
            self.log.emit(f"Source mtime:  {format_iso(st.st_mtime)}")
            self.log.emit(f"Source sha256: {sha256_file(self.target_py)}")
            self.log.emit("")
        except Exception as exc:
            self.log.emit(f"(could not compute source provenance: {exc})")
            self.log.emit("")

        # Resolve requirements path early, both flows use it.
        # V1.6 supports a custom requirements file. Blank falls back to
        # requirements.txt beside the selected target .py.
        requirements = (
            self.requirements_path if self.requirements_path
            else (self.target_py.parent / "requirements.txt")
        )
        has_requirements = bool(requirements and requirements.exists())

        # Single temp root: iso venv, runtime hooks, build stamp, spec/work all
        # live under it and are cleaned up together below.
        temp_root = Path(tempfile.mkdtemp(prefix="exe_toolbox_pyinstaller_"))
        workpath = temp_root / "work"
        specpath = temp_root / "spec"

        try:
            # V1.8: isolated build, throwaway venv installed from
            # requirements.txt only. Eliminates fat-shared-venv bloat where
            # transitive optional imports drag numpy/pandas/etc. into the EXE.
            if self.isolated_build:
                if not has_requirements:
                    raise RuntimeError(
                        "Isolated build needs a valid requirements.txt. Set one "
                        "explicitly, or place a requirements.txt beside the target .py."
                    )
                self.log.emit("Isolated build: creating clean venv (deps come only from requirements.txt)")
                self.python_cmd = create_isolated_venv(
                    self.python_cmd, temp_root / "iso_venv", log_func=self.log.emit
                )
                run_command(
                    self.python_cmd + ["-m", "pip", "install", "--upgrade", "pip"],
                    log_func=self.log.emit,
                )
                code = run_command(
                    self.python_cmd + ["-m", "pip", "install", "-r", str(requirements), "pyinstaller"],
                    cwd=self.target_py.parent,
                    log_func=self.log.emit,
                )
                if code != 0:
                    raise RuntimeError(f"Isolated venv install failed with exit code {code}.")
                self.log.emit("Isolated venv ready.")
                self.log.emit("")
            else:
                # Normal flow: install reqs on the user's selected Python.
                if self.install_requirements and has_requirements:
                    self.log.emit(f"Requirements file found: {requirements}")
                    code = run_command(
                        self.python_cmd + ["-m", "pip", "install", "-r", str(requirements), "pyinstaller"],
                        cwd=self.target_py.parent,
                        log_func=self.log.emit,
                    )
                    if code != 0:
                        raise RuntimeError(f"Requirements install failed with exit code {code}.")
                elif self.install_requirements:
                    self.log.emit(f"No requirements file found: {requirements}")

                if not python_has_module(self.python_cmd, "PyInstaller"):
                    if self.install_pyinstaller:
                        self.log.emit("PyInstaller is not installed for the selected Python. Installing it...")
                        code = run_command(
                            self.python_cmd + ["-m", "pip", "install", "--upgrade", "pyinstaller"],
                            cwd=self.target_py.parent,
                            log_func=self.log.emit,
                        )
                        if code != 0:
                            raise RuntimeError(f"PyInstaller install failed with exit code {code}.")
                    else:
                        raise RuntimeError("PyInstaller is not installed. Enable 'Install PyInstaller if missing'.")

                # V1.8: fat-venv warning. Only meaningful for the normal flow;
                # the isolated venv is lean by construction.
                bloat = heavy_libs_not_in_requirements(
                    self.python_cmd, requirements if has_requirements else None
                )
                if bloat:
                    self.log.emit("")
                    self.log.emit("WARNING: build interpreter has heavy libraries NOT in requirements.txt:")
                    for lib in bloat:
                        self.log.emit(f"  - {lib}")
                    self.log.emit("Transitive optional imports can drag these into the EXE and bloat it.")
                    self.log.emit("Enable 'Isolated build (lean venv from requirements.txt)' for a smaller exe.")
                    self.log.emit("")

            # Prepare icon.
            final_icon = None
            if self.icon_path:
                if not self.icon_path.exists():
                    raise FileNotFoundError(f"Icon file not found: {self.icon_path}")

                suffix = self.icon_path.suffix.lower()

                if suffix == ".ico":
                    final_icon = self.icon_path
                    self.log.emit(f"Using ICO icon: {final_icon}")
                elif suffix == ".png":
                    final_icon = self.icon_path.with_suffix(".ico")
                    self.log.emit("Icon is a PNG. Converting it to ICO first...")
                    convert_png_to_ico(
                        self.icon_path,
                        final_icon,
                        remove_light_border=self.png_remove_light_border,
                        remove_dark_border=self.png_remove_dark_border,
                        crop_square=self.png_crop_square,
                        padding_ratio=self.png_padding,
                        python_cmd=self.python_cmd,
                        log_func=self.log.emit,
                    )
                    self.log.emit(f"Using converted ICO icon: {final_icon}")
                else:
                    raise ValueError("Icon must be .ico or .png.")

            # Remove old EXE if possible.
            output_exe = self.output_dir / f"{self.exe_name}.exe"
            if output_exe.exists():
                try:
                    output_exe.unlink()
                    self.log.emit(f"Deleted old EXE: {output_exe}")
                except Exception as exc:
                    raise RuntimeError(
                        f"Could not delete old EXE. Is it still running?\n{output_exe}\n\n{exc}"
                    )

            runtime_hook_paths = []
            if self.appid_hook_enabled:
                app_id = self.app_user_model_id or default_app_user_model_id(self.exe_name)
                appid_runtime_hook_path = write_appid_runtime_hook(
                    temp_root / "runtime_hooks" / "set_appusermodelid.py",
                    app_id,
                )
                runtime_hook_paths.append(appid_runtime_hook_path)
                self.log.emit(f"Windows AppUserModelID runtime hook enabled: {app_id}")

            if self.pyqt_icon_hook_enabled:
                if final_icon:
                    pyqt_icon_runtime_hook_path = write_pyqt_icon_runtime_hook(
                        temp_root / "runtime_hooks" / "force_pyqt_window_icon.py",
                        final_icon.name,
                    )
                    runtime_hook_paths.append(pyqt_icon_runtime_hook_path)
                    self.log.emit(f"PyQt runtime window/taskbar icon hook enabled: {final_icon.name}")
                else:
                    self.log.emit("PyQt runtime icon hook requested, but no icon was selected; skipping it.")

            cmd = self.python_cmd + [
                "-m",
                "PyInstaller",
                "--noconfirm",
                "--name",
                self.exe_name,
                "--distpath",
                str(self.output_dir),
                "--workpath",
                str(workpath),
                "--specpath",
                str(specpath),
            ]

            if self.clean:
                cmd.append("--clean")

            if self.onefile:
                cmd.append("--onefile")
            else:
                cmd.append("--onedir")

            if self.windowed:
                cmd.append("--windowed")
            else:
                cmd.append("--console")

            if final_icon:
                cmd.extend(["--icon", str(final_icon)])

                # V1.5:
                # --icon embeds the Windows shell/taskbar/Explorer icon.
                # --add-data bundles the same .ico file inside the EXE so PyQt apps
                # can load it at runtime for the top-left window icon via sys._MEIPASS.
                add_data_arg = pyinstaller_add_data_arg(final_icon, ".")
                cmd.extend(["--add-data", add_data_arg])
                self.log.emit(f"Bundling icon for runtime window icon: {add_data_arg}")

            # V1.7: bundle declared non-Python data files/folders.
            # PyInstaller only auto-collects imported modules, so Flask/Jinja
            # templates/, static/, .env, etc. must be passed via --add-data with
            # the OS-correct separator. Missing these is the classic packaged
            # Flask "Internal Server Error" (template not found -> HTTP 500).
            if self.data_entries:
                self.log.emit("")
                self.log.emit("Bundling extra data files/folders:")
                for raw in self.data_entries:
                    resolved = resolve_data_entry(raw, self.target_py.parent)
                    if resolved is None:
                        continue
                    src, dest = resolved
                    add_data_arg = pyinstaller_add_data_arg(src, dest)
                    cmd.extend(["--add-data", add_data_arg])
                    self.log.emit(f"  --add-data {add_data_arg}")

            # V1.7: hidden imports PyInstaller cannot statically discover
            # (e.g. pystray._win32, the dynamically imported Windows tray backend).
            if self.hidden_imports:
                self.log.emit("")
                self.log.emit("Hidden imports:")
                for mod in self.hidden_imports:
                    cmd.extend(["--hidden-import", mod])
                    self.log.emit(f"  --hidden-import {mod}")

            # V1.8: embed EXE_TOOLBOX_BUILD.json so "which source produced this
            # exe?" is a one-second sha256 check instead of a multi-hour
            # misdiagnosis. Bundled at the EXE root.
            if self.embed_build_stamp:
                stamp_path = temp_root / "stamp" / BUILD_STAMP_FILENAME
                stamp = write_build_stamp(
                    stamp_path,
                    self.target_py,
                    self.exe_name,
                    self.python_cmd,
                    self.isolated_build,
                )
                cmd.extend(["--add-data", pyinstaller_add_data_arg(stamp_path, ".")])
                self.log.emit("")
                self.log.emit(f"Embedded build stamp: {BUILD_STAMP_FILENAME}")
                self.log.emit(f"  source_sha256: {stamp['source_sha256']}")
                self.log.emit(f"  built_at:      {stamp['built_at']}")
                self.log.emit(f"  pyinstaller:   {stamp['pyinstaller_version'] or '?'}")

            for runtime_hook_path in runtime_hook_paths:
                cmd.extend(["--runtime-hook", str(runtime_hook_path)])
                self.log.emit(f"Adding runtime hook: {runtime_hook_path}")

            if self.extra_pyinstaller_args:
                self.log.emit("")
                self.log.emit("Extra PyInstaller arguments:")
                self.log.emit(" ".join(self.extra_pyinstaller_args))
                cmd.extend(self.extra_pyinstaller_args)

            cmd.append(str(self.target_py))

            code = run_command(cmd, cwd=self.target_py.parent, log_func=self.log.emit)
            if code != 0:
                raise RuntimeError(f"PyInstaller failed with exit code {code}.")

            if output_exe.exists():
                self.log.emit("")
                self.log.emit("Build finished successfully.")
                self.log.emit(f"Created: {output_exe}")
                self.log.emit("")
                self.log.emit("If Windows Explorer still shows an old icon, that is usually icon cache.")
                self.log.emit("Try renaming the EXE, refreshing Explorer, or restarting Explorer.")
                self.finished_ok.emit(str(output_exe))
                return

            # In onedir mode, exe may be under dist/name/name.exe.
            onedir_exe = self.output_dir / self.exe_name / f"{self.exe_name}.exe"
            if onedir_exe.exists():
                self.log.emit("")
                self.log.emit("Build finished successfully.")
                self.log.emit(f"Created: {onedir_exe}")
                self.finished_ok.emit(str(onedir_exe))
                return

            raise RuntimeError("PyInstaller finished, but the expected EXE was not found.")

        finally:
            shutil.rmtree(temp_root, ignore_errors=True)


